class Dashboard extends React.Component {
  state = {
    open: false,
    offset: 0
  };

  componentDidMount = () => {
    this.props.getTools();
  };

  handleClick(offset) {
    console.log(offset);
    this.setState({ offset });
  }
  handleDrawerOpen = () => {
    this.setState({ open: true });
  };

  handleDrawerClose = () => {
    this.setState({ open: false });
  };

  render() {
    const { classes, history, clearUser } = this.props;
    if (!window.localStorage.token) {
      history.push("/login");
    }
    return (
      <div className={classes.root}>
        <CssBaseline />
        <AppBar
          position="absolute"
          className={classNames(
            classes.appBar,
            this.state.open && classes.appBarShift
          )}
        >
          <Toolbar
            disableGutters={!this.state.open}
            className={classes.toolbar}
          >
            <IconButton
              color="inherit"
              aria-label="Open drawer"
              onClick={this.handleDrawerOpen}
              className={classNames(
                classes.menuButton,
                this.state.open && classes.menuButtonHidden
              )}
            >
              <MenuIcon />
            </IconButton>
            <Typography
              component="h1"
              variant="h6"
              color="inherit"
              noWrap
              className={classes.title}
            >
              Dashboard
            </Typography>
            <IconButton
              onClick={() => {
                clearUser();
                window.localStorage.clear();
                history.push("/login");
              }}
              color="inherit"
            >
              <LockOutlinedIcon />
            </IconButton>
          </Toolbar>
        </AppBar>
        <Drawer
          variant="permanent"
          classes={{
            paper: classNames(
              classes.drawerPaper,
              !this.state.open && classes.drawerPaperClose
            )
          }}
          open={this.state.open}
        >
          <div className={classes.toolbarIcon}>
            <IconButton onClick={this.handleDrawerClose}>
              <ChevronLeftIcon />
            </IconButton>
          </div>
          <Divider />
          <List>{mainListItems}</List>
          <Divider />
        </Drawer>
        <main className={classes.content}>
          <div className={classes.appBarSpacer} />
          <Typography variant="h4" gutterBottom component="h2">
            Welcome {window.localStorage.firstname.toUpperCase()}!
          </Typography>
          <Route exact path="/dashboard" component={Items} />
          <Route
            exact
            path="/dashboard/tools/:id"
            render={props => <SingleTool {...props} />}
          />
          <Route exact path="/dashboard/mytools" component={MyTools} />
        </main>
      </div>
    );
  }
}

Dashboard.propTypes = {
  classes: PropTypes.object.isRequired
};

const mapStateToProps = state => ({
  loggedInUser: state.login.loggedInUser,
  tools: state.login.tools
});

export default connect(
  mapStateToProps,
  { clearUser, getTools }
)(withStyles(styles)(Dashboard));
